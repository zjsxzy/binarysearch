#define abs(x) ((x) > 0 ? (x) : -(x))
typedef long long LL;

int solve(int x, int y) {
    int z = x ^ y;
    int res = 0;
    while (z) {
        res += (z & 1);
        z >>= 1;
    }
    return res;
}

